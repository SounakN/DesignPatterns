package Behavioural.TemplatePattern;

public class Hooman implements Works {
	@Override
	public void pickup() {
		System.out.println("Hoooman pick up pen");
		
	}

	@Override
	public void Defend() {
		System.out.println("Hooman Defends Humanity");
		
	}

	@Override
	public void TheWhat() {
		System.out.println("Hooman then becomes Rotten King");
		
	}
}
